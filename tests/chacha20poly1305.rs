use cryptal::encryption::chacha20poly1305::{Chacha20Poly1305Error, auth, decrypt, encrypt};

#[test]
fn test_poly1305_rfc8439_vector() {
    let key: [u8; 32] = [
        0x85, 0xd6, 0xbe, 0x78, 0x57, 0x55, 0x6d, 0x33, 0x7f, 0x44, 0x52, 0xfe, 0x42, 0xd5, 0x06,
        0xa8, 0x01, 0x03, 0x80, 0x8a, 0xfb, 0x0d, 0xb2, 0xfd, 0x4a, 0xbf, 0xf6, 0xaf, 0x41, 0x49,
        0xf5, 0x1b,
    ];

    let msg = b"Cryptographic Forum Research Group";

    let mut tag = [0u8; 16];
    auth(&mut tag, &key, msg);

    let expected: [u8; 16] = [
        0xa8, 0x06, 0x1d, 0xc1, 0x30, 0x51, 0x36, 0xc6, 0xc2, 0x2b, 0x8b, 0xaf, 0x0c, 0x01, 0x27,
        0xa9,
    ];

    assert_eq!(tag, expected, "Poly1305 tag mismatch");
}

#[test]
fn test_chacha20poly1305_empty_aad() {
    let key: [u8; 32] = [
        0x80, 0x81, 0x82, 0x83, 0x84, 0x85, 0x86, 0x87, 0x88, 0x89, 0x8a, 0x8b, 0x8c, 0x8d, 0x8e,
        0x8f, 0x90, 0x91, 0x92, 0x93, 0x94, 0x95, 0x96, 0x97, 0x98, 0x99, 0x9a, 0x9b, 0x9c, 0x9d,
        0x9e, 0x9f,
    ];

    let nonce: [u8; 12] = [
        0x07, 0x00, 0x00, 0x00, 0x40, 0x41, 0x42, 0x43, 0x44, 0x45, 0x46, 0x47,
    ];

    let plaintext: &[u8] = b"Ladies and Gentlemen of the class of '99: \
If I could offer you only one tip for the future, sunscreen would be it.";

    let mut ciphertext = vec![0u8; plaintext.len()];
    let mut tag = [0u8; 16];

    encrypt(&key, &nonce, plaintext, &mut ciphertext, &mut tag);

    assert_eq!(
        &ciphertext[..16],
        &[
            0xd3, 0x1a, 0x8d, 0x34, 0x64, 0x8e, 0x60, 0xdb, 0x7b, 0x86, 0xaf, 0xbc, 0x53, 0xef,
            0x7e, 0xc2
        ],
        "ciphertext mismatch"
    );

    let mut decrypted = vec![0u8; plaintext.len()];
    let res = decrypt(&key, &nonce, &ciphertext, &tag, &mut decrypted);

    assert!(res.is_ok(), "decryption should succeed");
    assert_eq!(decrypted, plaintext, "plaintext mismatch");

    let mut bad_tag = tag;
    bad_tag[0] ^= 1;
    let res_bad = decrypt(&key, &nonce, &ciphertext, &bad_tag, &mut decrypted);
    assert!(
        matches!(res_bad, Err(Chacha20Poly1305Error::AuthenticationFailed)),
        "should reject bad tag"
    );
}

